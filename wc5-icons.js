function setup() {
  createCanvas(windowWidth, windowHeight)
  background(31)
  let s = floor(grid.blockSize)
  let cnvwid = grid.cols * s
  let cnvhei = grid.rows * s
  let patterns = [stat0, stat1, houses]
  let dx = 0
  for (group of patterns) {
    for (icon of Object.keys(group)) {
      let cnv = createGraphics(cnvwid, cnvhei)
      cnv.stroke(31)
      cnv.noStroke()
      for (let col = 0; col < grid.cols; col++) {
        for (let row = 0; row < grid.rows; row++) {
          group[icon][col][row] == 1 ? cnv.fill(223) : cnv.fill(31)
          console.log()
          cnv.rect(row * s, col * s, s) // , s, s / 3)
        }
      }
      image(cnv, 0, 0)
      dx += cnvwid
      translate(cnvwid, 0, 0)
    }
    translate(-dx, cnvhei)
    dx = 0
  }
}
